package org.neuedu.hisjava.model;

public class CountAndSum {
    private Integer Count;
    private Double Sum;

    public Integer getCount() {
        return Count;
    }

    public void setCount(Integer count) {
        Count = count;
    }

    public Double getSum() {
        return Sum;
    }

    public void setSum(Double sum) {
        Sum = sum;
    }
}
